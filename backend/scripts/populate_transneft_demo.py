async def main():
    client = AsyncIOMotorClient(MONGO_URL)
    await init_beanie(
        database=client[DB_NAME],
        document_models=[Search, Resume]
    )

    # 1. Delete mock "Иван Иванов" resumes
    mock_deleted = await Resume.find({"raw_data.id": "mock_resume_1"}).delete()
    print(f"Deleted mock resumes: {mock_deleted.deleted_count}")

    # 2. Delete old non-Транснефть searches and their resumes
    old_search_queries = ["Python developer", "Слесарь-сантехник"]
    old_searches = await Search.find({"query": {"$in": old_search_queries +
        ["Python developer".lower(), "слесарь-сантехник"]}}).to_list()
    # Broader match
    all_searches = await Search.find_all().to_list()
    old_search_ids = []
    for s in all_searches:
        if any(q.lower() in s.query.lower() for q in old_search_queries):
            old_search_ids.append(str(s.id))

    for sid in old_search_ids:
        r_del = await Resume.find({"search_id": sid}).delete()
        print(f"Deleted {r_del.deleted_count} resumes for old search {sid}")
        s = await Search.get(sid)
        if s:
            await s.delete()
            print(f"Deleted old search {sid}")

    # 3. Populate Транснефть resumes with realistic data
    tneft_searches = await Search.find({"query": {"$regex": "нефт|трубопровод|НПС|сварщик|электромонтёр|диспетчер|слесарь|главный инженер|охрана труда", "$options": "i"}}).to_list()

    total_updated = 0
    for search in tneft_searches:
        role = get_role_for_search(search.query)
        resumes = await Resume.find({"search_id": str(search.id)}).to_list()

        # Keep up to 25 resumes per search, delete the rest
        if len(resumes) > 25:
            to_delete = resumes[25:]
            for r in to_delete:
                await r.delete()
            resumes = resumes[:25]

        print(f"Processing {len(resumes)} resumes for '{search.query[:60]}' (role: {role})")

        for i, resume in enumerate(resumes):
            data = generate_resume_data(role, i + hash(str(search.id)) % 100)
            ai = data["ai_analysis"]

            resume.title = data["title"]
            resume.city = data["city"]
            resume.age = data["age"]
            resume.salary = data["salary"]
            resume.currency = data["currency"]
            resume.raw_data = {
                **resume.raw_data,
                "title": data["title"],
                "area": {"name": data["city"]},
                "age": data["age"],
                "salary": {"amount": data["salary"], "currency": "RUR"},
                "skills": [{"name": s} for s in data["skills"]],
                "experience": data["experience"],
                "education": [data["education"]],
                "first_name": "",  # Keep anonymized
                "last_name": "",
            }
            resume.ai_score = ai["score"]
            resume.match_percentage = ai["match_percentage"]
            resume.ai_summary = ai["summary"]
            resume.match_explanation = ai["match_explanation"]
            resume.strengths = ai["strengths"]
            resume.weaknesses = ai["weaknesses"]
            resume.recommendation = ai["recommendation"]
            resume.ai_questions = ai["questions"]
            resume.red_flags = ai["red_flags"]
            resume.ai_generated_detected = ai["ai_generated_detected"]
            resume.evaluation_details = ai["evaluation_details"]
            resume.interview_focus = ai["interview_focus"]
            resume.career_trajectory = ai["career_trajectory"]
            resume.analyzed = True

            await resume.save()
            total_updated += 1

        # Update search stats
        search.status = "completed"
        search.analyzed_count = len(resumes)
        search.total_found = len(resumes)
        await search.save()

    print(f"\nTotal resumes updated: {total_updated}")
    print("Demo data population complete!")
    client.close()

if __name__ == "__main__":
    asyncio.run(main())
